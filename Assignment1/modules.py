        self.grads = {'weight': None, 'bias': None} # Gradients

        #######################
        # PUT YOUR CODE HERE  #
        #######################
        self.node_in = in_features
        self.node_out = out_features
        if input_layer:
          limit = np.sqrt(1 / float(self.node_in)) # Gain value for input layers = 1
        else:
          limit = np.sqrt(2 / float(self.node_in)) # Gain value for non-input layers with non-linear activation (Relu) = 2
        self.params['weight'] = np.random.normal(0.0, limit, size=(self.node_out, self.node_in)) #np.random.randn(self.node_in, self.node_out) * math.sqrt(2 / self.node_out)
        self.params['bias'] = np.zeros((self.node_out, 1))
        self.grads['weight'] = np.zeros((self.node_out, self.node_in))
        self.grads['bias'] = np.zeros((self.node_out, 1))
        self.cache_x = None
        #######################
        # END OF YOUR CODE    #
        #######################

